


                                              // Vector //



// 1) Vector is like a resizable array in Java.

// 2) It is present in the java.util package.

// 3) It stores data in order (index form) — like 0, 1, 2, 3....

// 4) It can grow automatically when it becomes full.

// 5) Default size = 10 elements.

// 6) When full, its size doubles automatically.

// 7) It allows duplicate values.

// 8) It allows null values.

// 9) It is synchronized — means thread safe (only one thread works at a time).

// 10) It is slower than ArrayList because of synchronization.

// 11) It is an old (legacy) class (from Java 1.0). 

// 12) We can add, remove, update, and access elements easily using methods.

// 13) We can go through elements using for loop, iterator, or enumeration.